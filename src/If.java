public class If{
    public static void main (String[]args){
        int numerOf = 0;

        if (numerOf < 0){
            System.out.println("El numero es Negativo");
        } else if (numerOf > 0){
            System.out.println("El numero es Positivo");
        } else{
            System.out.println("El numero es 0");
        }
    }
}
